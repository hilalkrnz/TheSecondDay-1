package Homework2;

public class Course {
	
	int courseID;
	String courseName;
	String instructor;
	String detail;

		
	public Course() {
		
	}

	public Course(int courseID, String courseName, String instructor, String detail) {
		
		this.courseID = courseID;
		this.courseName = courseName;
		this.instructor = instructor;
		this.detail = detail;
	}

}
